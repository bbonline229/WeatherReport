import UIKit

extension UIColor {
    @nonobjc class var darkBlue: UIColor {
        return UIColor(red: 36 / 255.0, green: 74.0 / 255.0, blue: 96.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var skyBlue: UIColor {
        return UIColor(red: 9 / 255.0, green: 130.0 / 255.0, blue: 231.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var otherBlue: UIColor {
        return UIColor(red: 63 / 255.0, green: 136.0 / 255.0, blue: 197.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var specialGray: UIColor {
        return UIColor(red: 228 / 255.0, green: 228.0 / 255.0, blue: 228.0 / 255.0, alpha: 0.3)
    }
}
